            <p>NCNU is located in Puli Township, Nantou County, the geographic center of Taiwan. Since the school’s establishment in 1995, the mission was to globalize higher education, integrate innovative teaching and research outcomes, and then use digital technology to connect with the global academic communities and collaborate with more than 100 foreign universities to create a strategy for global layout and dialogue.</p>

            <p>NCNU's academic exchange network stimulates interdisciplinary sharing and collaboration, such as discussing the green economy and local creation issues with Japan's National Graduate Institute for Policy Studies and Niigata University, holding digital conferences on computer animation with Meiji University and Kansai University in Japan, and discussing post-disaster reconstruction with Shinshu University. NCNU has also participated in constructing water purification facilities in rural Cambodia and holds international education forums and seminars frequently to strengthen academic research.</p>

